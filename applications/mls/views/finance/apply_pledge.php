<script>
    window.onload = function(){

        setInterval(function(){

            var winHeight = $(window).height()-43;
            $('.zws_scroll').css('height',winHeight);
        },30);
        //性别
        $('.ajd_sex input').click(function(){

            $(this).parents('.bv').siblings().find('.bot').removeClass('checked');
            $(this).parent('.bot').addClass('checked');
        })
    }

    $(document).ready(function(){
        $(".form-horizontal").validate({
            focusInvalid: true, //当为false时，验证无效时，没有焦点响应
            onkeyup: false,
            submitHandler: function(form){   //表单提交句柄,为一回调函数，带一个参数：form
    			$.ajax({
    				type: 'POST',
    				url: '/finance/apply_pledge/',
    				data:$(form).serialize(),
    				dataType: 'json',
    				success: function(data){
    				    if(data.result == 'ok'){
    				        var borrower = $(form).find("input[name='borrower']").val();
    				        var block_name = $(form).find("input[name='block_name']").val();
    				        var intentional_money = $(form).find("input[name='intentional_money']").val();
                            $.zws_jr_pop_title({'msg':data.msg,'borrower':borrower,'block_name':block_name,'intentional_money':intentional_money,'id':'0'});
    				    }else{
        				    $('#js_prompt').html(data.msg);
        				    openWin('js_pop');
    				    }
    				}
    			});
                return false;
            },
            rules: {
              borrower: "required",
              phone: "required",
              block_name: "required",
              price: "required",
              intentional_money: "required"
            },
            messages: {
              borrower: "请输入您的名字",
              phone: "请输入您的电话",
              block_name: "请输入小区名称",
              price: "请输入房屋总价",
              intentional_money: "请输入意向额度 "
            }
        });
    });

    $.zws_jr_pop_title = function(options){
        var msg = options.msg || '';
        var borrower = options.borrower || '';
        var block_name = options.block_name || '';
        var intentional_money = options.intentional_money || '';
        var id = options.id || '';

        var _show = $('.zws_jr_pop');
        var html = '<dl class="zws_jr_pop_title"><dd>提示</dd><dt>X</dt></dl><div class="zws_jr_pop_contcent"><span><img src="<?php echo MLS_SOURCE_URL;?>/mls/images/v1.0/dyd/zws_sucess_03.jpg" alt=""/><b>{msg}</b></span><p>姓名：{borrower}<br/>小区：{block_name}<br/>意向额度：{intentional_money}万</p></div><span class="zws_jr_pop_btn"><a href="/finance/my_customer" class="pop_jr_pr">查看进度</a><a href="javascript:void(0)">关闭</a></span>';

        html = html.replace('{msg}',msg);
        html = html.replace('{borrower}',borrower);
        html = html.replace('{block_name}',block_name);
        html = html.replace('{intentional_money}',intentional_money);
        html = html.replace('{id}',id);
        _show.html(html);
        _show.show();
        _show.find('.zws_jr_pop_title dt').on('click',function(){
            _show.hide();
        });
        _show.find('.zws_jr_pop_btn a:last').on('click',function(){
            _show.hide();
        });
    }


</script>
