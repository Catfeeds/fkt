</p>
<p>
    <span style="font-size: 14px;">4、乙方承诺自本协议签订之日起90日内不与甲方业主发生直接联系，不通过其他渠道非法获取此房源信息，承诺不与业主互留电话及其它联系方式，所有相关谈判必须通过甲方与业主进行沟通。</span>
</p>
<p>
    <span style="font-size: 14px;">5、如乙方发现甲方的房源信息虚假或发现甲方通过其他渠道直接或间接接触乙方客户，并试图获取该客户信息，取证取得确凿证据（必须有但不限于客户书面证明、乙方录音等）后，经双方协商无法解决的，则乙方可向丙方投诉，丙方有权要求双方无条件配合核实所有信息，一经查实，丙方视情节轻重，有权给予违约方警告公示、降级、暂停帐号、直至永久关闭帐号等处罚，同时将结果通知甲、乙双方。</span>
