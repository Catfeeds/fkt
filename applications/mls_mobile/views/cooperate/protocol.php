    <span style="font-size: 14px;">七、其他约定：</span>
</p>
<p>
    <span style="font-size: 14px;">1、甲、乙双方保证遵守丙方关于合作的规章条款。甲乙双方可根据具体情况单独约定其他商务条件，丙方对甲乙双方私下达成的协议或约定不承担任何责任。一旦双方在私下达成合作协议和承诺，丙方将不承担本协议约定的应由丙方承担的责任。</span>
</p>
<p>
    <span style="font-size: 14px;">2、本次交易的法律责任由甲、乙双方根据佣金分配比例各自承担相应的风险责任；如任一方违反国家法律法规造成经济纠纷和法律责任由违反方自行承担。</span>
</p>
<p>
    <span style="font-size: 14px;">3、甲乙双方必须如实及时沟通并在合作平台更新状态，不得采用隐瞒事实真相、串通欺骗等非法手段或枉顾同行利益,通过他人或单位完成交易并私吞服务报酬, 一经查实，丙方视情节轻重，有权给予违约方警告公示、降级、暂停帐号、直至永久关闭帐号等处罚，同时违约方必须按照以上服务报酬分配金额双倍赔偿给守约方。</span>
</p>
<p>
    <span style="font-size: 14px;">4、甲乙方按照本合同约定开展合作的，应遵守本合同的约定，如因一方违约，给另一方造成损失的，守约方可要求违约方赔偿，同时，丙方可按照本合同约定对违约方进行处罚，但丙方不对任一方的违约行为向守约方承担任何责任。</span>
</p>
<p>
    <span style="font-size: 14px;">5、甲、乙、丙三方均应按照国家有关税法规定自行纳税。</span>
</p>
