<p>
    <span style="font-size: 14px;">六、丙方权利义务：</span>
</p>
<p>
    <span style="font-size: 14px;">1、丙方承诺秉持公平、公正、公开的第三方立场，不偏袒合作中的任何一方。</span>
</p>
<p>
    <span style="font-size: 14px;">2、丙方作为第三方平台，仅为双方提供信息展示、推荐、匹配服务；对于甲乙双方与客户或甲乙双方的客户在房产交易过程中的产生的任何纠纷，丙方不负任何责任；</span>
</p>
<p>
    <span style="font-size: 14px;">3、甲乙双方就本次合作的相关事宜，双方确认并认可丙方或丙方所认可的经纪人联盟的权威裁决，并无条件的遵守其作出的裁决。</span>
