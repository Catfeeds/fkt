</p>
<p>
    <span style="font-size: 14px;">一、合作事项</span>
</p>
<p>
    <span style="font-size: 14px;">1、<strong>合作标的物业：{house_detail}</strong>：</span>
</p>
<p>
    <span style="font-size: 14px;">2、合作事由：甲乙双方本着互惠互利原则，甲方提供标的物业房源，乙方向甲方推荐意向客户，双方共同促进此物业的成交。</span>
</p>
<p>
    <span style="font-size: 14px;">3、双方约定由（√甲方 &nbsp;√乙方 &nbsp;√共同）完成：带客户看房、签约、贷款、过户等相关手续。</span>
</p>
<p>
    <span style="font-size: 14px;"><br/></span>
</p>
<p>
    <span style="font-size: 14px;">二、佣金分配方式：</span>
</p>
<p>
    <span style="font-size: 14px;">1、双方约定本次交易的居间佣金的总额（指本次交易的买卖双方分别支付给甲、乙双方的佣金总额）按照<strong><span style="font-size: 14px; text-decoration: underline; color: rgb(227, 108, 9);">50%:50%</span></strong> 进行合作分成。佣金须等买卖双方物业交验完成后方可分配。</span>
</p>
<p>
    <span style="font-size: 14px;">2、佣金分配后,若因买卖双方原因导致该服务项目有变化,佣金需要退还时,甲乙双方均须如数退还佣金,否则所产生任何法律诉讼及纠纷时,不肯退费方应自行承担所有经济纠纷及法律责任。</span>
</p>
<p>
    <span style="font-size: 14px;"><br/></span>
</p>
<p>
    <span style="font-size: 14px;">三、关于赏金</span>
</p>
<p>
    <span style="font-size: 14px;">1、甲乙双方为了能尽快促成成交，可发布针对对方的“赏金”；赏金为佣金以外的合作费用；在买卖双方物业交验完成后由发布方支付。</span>
</p>
<p>
    <span style="font-size: 14px;">2、首次发布的赏金金额不得低于人民币壹千元；否则，丙方作为合作发布的第三方平台，有权不予以发布；</span>
</p>
<p>
    <span style="font-size: 14px;">3、赏金发布方在合作过程中，可根据实际情况随时调整赏金金额，但不得低于初次发布和上次发调整后的金额；</span>
</p>
<p>
    <span style="font-size: 14px;">4、赏金发布方在与其他中介方达成居间合作之前，可根据实际情况随时取消赏金奖励，甲乙双方均不得就此有异议；居间合作达成后，已发布的赏金不得取消，发布方应据实支付赏金费用。</span>
</p>
<p>
    <span style="font-size: 14px;"><br/></span>
</p>
<p>
    <span style="font-size: 14px;">四、甲方权利义务：</span>
</p>
<p>
    <span style="font-size: 14px;">1、甲方必须保证所提供的物业信息真实有效。</span>
</p>
<p>
    <span style="font-size: 14px;">2、甲方负责联系合作房源的业主，安排看房等事宜。</span>
</p>
<p>
    <span style="font-size: 14px;">3、甲方为双方合作完成本次交易提供相应的便利和协助。</span>
</p>
<p>
    <span style="font-size: 14px;">4、甲方承诺自本协议签订之日起90日内不与乙方提供的客户发生直接联系，不通过其他渠道非法获取客户信息，承诺不与客户互留电话及其它联系方式，所有相关谈判必须通过乙方与客户进行沟通。</span>
</p>
<p>
    <span style="font-size: 14px;">5、如甲方发现乙方的客户信息虚假或发现乙方通过其他渠道直接或间接接触甲方业主，并试图获取此房源信息，取得确凿证据（必须有但不限于客户书面证明、乙方录音等）后，经双方协商无法解决的，则甲方可向丙方投诉，丙方有权要求双方无条件配合核实所有信息。一经查实，丙方视情节轻重，有权给予违约方警告公示、降级、暂停帐号、直至永久关闭帐号等处罚，同时将结果通知甲、乙双方。</span>
</p>
<p>
    <span style="font-size: 14px;"><br/></span>
</p>
<p>
    <span style="font-size: 14px;">五、乙方权利义务：</span>
</p>
<p>
    <span style="font-size: 14px;">1、乙方保证乙方推荐的客户真实有效。</span>
</p>
<p>
    <span style="font-size: 14px;">2、乙方负责联系客户，安排看房事宜。</span>
</p>
<p>
